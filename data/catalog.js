const mockProduct = async (product, type) => {
  const imageUri = Image.resolveAssetSource(product.image).uri;

  fetch(imageUri)
    .then((res) => res.blob())
    .then(async (blob) => {
      const file = new File([blob], "image.jpg", { type: "image/jpeg" });
      const newProduct = {
        ...product,
        image: file,
        type: type,
        waste: { // randomize waste
          textile: Math.floor(Math.random() * 20) / 10,
          water: Math.floor(Math.random() * 20) / 10,
          carbon: Math.floor(Math.random() * 10) / 10,
        }
      };
      await createProduct(newProduct);
    })
    .catch((err) => {
      console.log(err);
    });
};

